
package com.plan_it_urban.plan_it_urban.Model;

public class Department {
    private int dept_id;
    private String dept_name;
    private String dept_code;
    private String dept_state;
    private String dept_city;


    public int getDept_id() { return dept_id; }
    public void setDept_id(int dept_id) { this.dept_id = dept_id; }

    public String getDept_name() { return dept_name; }
    public void setDept_name(String dept_name) { this.dept_name = dept_name; }

    public String getDept_code() { return dept_code; }
    public void setDept_code(String dept_code) { this.dept_code = dept_code; }

    public String getDept_state() { return dept_state; }
    public void setDept_state(String dept_state) { this.dept_state = dept_state; }

    public String getDept_city() { return dept_city; }
    public void setDept_city(String dept_city) { this.dept_city = dept_city; }
}
